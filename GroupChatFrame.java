    private void sandButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sandButtonActionPerformed
        String msg = msgEditor.getText();
        if (msg.trim().length() > 0) {
            msgTextArea.append(MainFrame.user + "\t" + new Date() + "\n" + msg
                    + "\n--------------------------------------------------------------------------------\n");
            msgEditor.setText("");
            for (int i = 0; i < 5; i++) {
                if (MainFrame.sander.write("ChatWithGroup\n" + group + "\n" + msg).equalsIgnoreCase("ok")) {
                    break;
                }
                msgTextArea.append("消息发送失败，正在重试\n");
                if (4 == i) {
                    msgTextArea.append("消息发送失败，出现了问题\n");
                }
            }
        }
    }//GEN-LAST:event_sandButtonActionPerformed

